package p05FilterByAge.Person;

public class Person {

    private String name;
    private int age;

    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return this.age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getPrintCondition(String condition) {
        if (condition.equals("age")) {
            return this.age + "";
        }

        return this.name;
    }

}
